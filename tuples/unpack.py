# list
fruits = ["apple", "banana", "cherry"]
apple, banana, cherry = fruits

print(apple)
print(banana)
print(cherry)

# Tuple
fruits = ("apple", "banana", "cherry")

# แบบที่1
(apple, banana, cherry) = fruits

# แบบที่2
apple = fruits[0]
banana = fruits[1]
cherry = fruits[2]

print(apple)
print(banana)
print(cherry)